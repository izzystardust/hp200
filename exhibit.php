function update(index, element) {
  // if last element, change "next" button to "beginning"
  // if first element, disable "previous"
  if (mySwipe.getNumSlides() == index + 1) {
    document.getElementById("next").innerHTML = "Beginning";
	document.getElementById("prev").disabled = false;
  } else if (index == 0) {
    document.getElementById("prev").disabled = true;
    document.getElementById("next").innerHTML = "Next";
  } else {
    document.getElementById("next").innerHTML = "Next";
    document.getElementById("prev").disabled = false;
  }
  
  window.location.hash = index + 1;
  
}

document.onkeydown = function(e) {
  e = e || window.event;

    if (e.keyCode == '37') {
      if (mySwipe.getPos() != 0) {
        mySwipe.prev();
      }
    }
    else if (e.keyCode == '39') {
      if (mySwipe.getPos() != mySwipe.getNumSlides() - 1) {
        mySwipe.next();
      }
    }
}

var elem = document.getElementById('mySwipe');
window.mySwipe = Swipe(elem, {
  callback: update,
});

if (window.location.hash) {
  var hash = window.location.hash.substring(1);
  mySwipe.slide(parseInt(hash, 10) - 1, 400);
}

