	</div>
	
    <div>
      <h2>Beginnings of the Controversy</h2>
      <p>Controversy arose in 1920, when the Black River Regulating District put forward a proposal for nine new reservoirs, some of which called for the flooding of land located in the <a rel="#forest">Forest Preserve</a>.  According to the <a rel="#foreverwild">Forever Wild</a> clause in the New York State Constitution, the Forest Preserve was to be protected and not to be used by any public or private entities.  The two most controversial proposals called for the construction of reservoirs at Higley and Panther Mountains, both located on the South Branch of the Moose River, a tributary to the Black River. The construction of these two dams would have flooded an area of approximately 5,000 acres in the <a rel="#mooseriverplains">Moose River Plains</a>.</p>
    </div>

    <div>
      <img src="img/area-flooding.png" />
	  <h2>The Flooding</h2>
	  <p>The highlighted portion of the map indicates the area that would have been flooded, a region of almost 8 square miles! This land was largely made up of the <a rel="#mooseriverplains">Moose River Plains</a>, a major breeding ground for deer and other wildlife in the Adirondacks.</p>
    </div>

    <div>
      <h2>Opposition</h2>
      <p>The <a rel="#conservation_dept">Conservation Department</a> approved the proposal for construction of new reservoirs in 1945.  Much of the opposition to the construction of the reservoirs came about because of two men, <a rel="#apperson">John S. Apperson</a> and <a rel="#schaefer">Paul Schaefer</a>. In 1946, Apperson and Schaefer organized the <a rel="#mooserivercommittee">Adirondack Moose River Committee</a> to fight the construction of the dams. Apperson and Schaefer did everything they could to raise awareness about what the reservoirs would do to the <a rel="#mooseriverplains">Moose River Plains</a>, including disseminating fliers and movies.</p>
    </div>
	
    <div>
      <h2>Legal Battles</h2>
      <p>As public awareness grew, the <a rel="#mooserivercommittee">Adirondack Moose River Committee</a>, the <a rel="#wildernesssociety">Wilderness Society</a>, the <a rel="#conservation_dept">Conservation Department</a>, and hundreds of other organizations across the country joined the opposition to the reservoirs.  The controversy over construction of the reservoirs ultimately went all the way to the Supreme Court and is regarded as a “legendary battle” among environmentalists.  </p>
    </div>
    <div>
      <h2>Specifics of Lawsuits</h2>
	  <p>The <a rel="#dewey">governor</a> initially supported the plan to construct the proposed dams. However, the opposition led by <a rel="#apperson">Apperson</a> and <a rel="#schaefer">Schaefer</a> persuaded the governor to change his mind, resulting in the passing of the <a rel="#stokes">Stokes Act</a> in 1950. The regulating district felt that said act was unlawful and unjust for many reasons.</p>
	  <p>One of the main opposing points was the investment of money and man-hours necessary to survey and prepare the area for the project. Due to this and other factors, the district filed a lawsuit opposing the act. This lawsuit (<a href="http://www.amazon.com/Regulating-Appellant-Adirondack-Transcript-Supporting/dp/1270418157" target="_blank">Adirondack League Club v. Board of Black River Regulating District</a>) was rejected by the New York Supreme Court on the basis that taking public property for private interests was unconstitutional, and that building a reservoir for hydroelectric power represented a private interest. The regulating district appealed the decision to the United States Supreme Court in 1950, where the original ruling was upheld.</p>
    </div>
    <div>
      <h2>Ramifications of the Lawsuit</h2>
      <p>The Ostrander Amendment was proposed by the state legislature in 1951, as a way to undo the <a rel="#burd">Burd Ammendment</a>. It stated that no dam or reservoir could be built on <a rel="#forest">Forest Preserve</a> land. The amendment was ratified by voters of New York in 1953 by a 59% majority. In 1955, the Regulating District proposed a constitutional amendment specifically allowing the Panther Dam to be built. The amendment was ultimately rejected by New York voters by a 3 to 1 margin.</p>
    </div>
	
    <div>
      <h2>Impact on Environmental Awareness</h2>
      <p>In 1955, thanks to grass-roots activism, a total of 38 proposals for reservoirs in the Adirondacks were defeated. Environmentalists such as Apperson and Schaefer were involved in many projects besides protecting the Moose River Plains. Together, they accounted for much of the leadership behind many movements opposing the further human development of the Adirondacks in the 20th century. These included opposing the <a href="http://www.adirondackalmanack.com/2013/02/lake-george-robert-moses-john-apperson-and-tongue-mountain.html" target="_blank">development of the shore of Lake George</a>, opposing the development of a ski slope on Whiteface Mountain, and successfully fighting to find a more <a href="http://www.dec.ny.gov/pubs/47648.html" target="_blank">sustainable route for I-87</a>.</p>
    </div>
